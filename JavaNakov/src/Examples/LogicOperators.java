package Examples;

public class LogicOperators {
	
	public static void main(String[] args){
		
	boolean a = true;
	boolean b = false;
	System.out.println(a && b); // false
	System.out.println(a || b); // true
	
	// De Morgan theorems 
	
	
	String s = "Bear";
	System.out.println(s instanceof String);
  }
}
