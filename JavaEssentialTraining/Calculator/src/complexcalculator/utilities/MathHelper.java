package complexcalculator.utilities;


public class MathHelper {

    public static void addition(double number, double secondNumber) {
        System.out.println("The answer is: " +(number + secondNumber));
    }

    public static void subtraction(double number, double secondNumber) {
        System.out.println("The answer is: " + (number - secondNumber));
    }

    public static void multiplication(double number, double secondNumber) {
        System.out.println("The answer is: " + (number * secondNumber));
    }

    public static void division(double number, double secondNumber) {
        System.out.println("The answer is: " + (number / secondNumber));
    }
}
